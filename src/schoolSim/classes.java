package schoolSim;

public class classes {
    public int x, y, floorNum;

    public classes(int x, int y, int floorNum)
    {
        this.x = x;
        this.y = y;
        this.floorNum = floorNum;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
    public int getfloorNum() {  	
    	return floorNum;
    }
    
}